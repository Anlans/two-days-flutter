巫哲


巫哲的笔名一开始听上去像男生，也挺文艺范。听说，巫哲取这个笔名，是因为一开始想写玄幻文，结果写的文大多都是近现代的，文风一直很稳定，以真实、细腻、治愈为主。

巫哲的笔名挺好的，给自家角色起名就比较搞笑了，都有规律可循。

比如带方位的：沈东、边南、项西、徐北、顾中。


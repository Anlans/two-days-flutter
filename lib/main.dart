淮上


淮上的名字听上去是不是特别高大上、文艺范？有粉丝称淮上的名字由来，是因为淮上喜欢吃“淮上汤包”。还有粉丝称是因为淮上小时候家附近有一家“淮上包子铺”。不管是哪个原因，瞬间打破了缘原有的滤镜，直接降低了一个档次，变得朴实接地气了。同时，也补充了一个小知识，那就是“淮上”还是一个地名。


